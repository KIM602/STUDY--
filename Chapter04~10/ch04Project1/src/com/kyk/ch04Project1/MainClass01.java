public class MainClass01 {

	public static void main(String[] args) {
		int score = 80;
		if(score > 60) {
			//if문의 ()안에는 조건식이 들어가며 조건식은 true나 false를 반환하는 비교 연산이나 논리 연산이어야 한다.
			//조건식이 true인 경우에 실행
			System.out.println("합격입니다.");
		}
		
		System.out.println("if문 다음문장입니다.");
	}

}
